package by.mkwt.anthill.service.exception;

public class DonationException extends Exception {

	public DonationException() {
		super();
	}

	public DonationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public DonationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DonationException(String arg0) {
		super(arg0);
	}

	public DonationException(Throwable arg0) {
		super(arg0);
	}

}
